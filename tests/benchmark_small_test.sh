#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$ROOT_DIR"

CONFIG_BACKUP="/tmp/dsa_project01_config_backup_benchmark_$$.txt"
GENERATOR_BIN="/tmp/dsa_project01_data_generator_benchmark_$$"
MAIN_BIN="/tmp/dsa_project01_main_benchmark_$$"
OUTPUT_FILE="/tmp/dsa_project01_benchmark_output_$$.txt"
BENCH_DATASET="/tmp/dsa_project01_benchmark_dataset_$$.csv"
INDEX_PREFIX="/tmp/dsa_project01_benchmark_index_$$_"

restore_config() {
    if [[ -f "$CONFIG_BACKUP" ]]; then
        cp "$CONFIG_BACKUP" config.txt
    fi
}

trap restore_config EXIT

assert_output_contains() {
    local needle="$1"
    if ! grep -Fq "$needle" "$OUTPUT_FILE"; then
        echo "FAIL: expected benchmark output to contain: $needle" >&2
        echo "---- benchmark output ----" >&2
        cat "$OUTPUT_FILE" >&2
        echo "--------------------------" >&2
        exit 1
    fi
}

assert_result_count() {
    local n="$1"
    local expected="$2"
    local actual
    actual="$(grep -F "RESULT | N=$n |" "$OUTPUT_FILE" | wc -l)"
    if [[ "$actual" -ne "$expected" ]]; then
        echo "FAIL: expected $expected benchmark RESULT lines for N=$n, got $actual" >&2
        cat "$OUTPUT_FILE" >&2
        exit 1
    fi
}

cp config.txt "$CONFIG_BACKUP"

cat > config.txt <<CONFIG
# Generated by tests/benchmark_small_test.sh
dataset_file=$BENCH_DATASET
generated_record_count=300
query_count=100
data_sizes=100,300
range_threshold=200
range_small=7
range_large=13
index_prefix=$INDEX_PREFIX
CONFIG

g++ data_generator.cpp -o "$GENERATOR_BIN"
"$GENERATOR_BIN"

g++ Solution/main.cpp Solution/bplus_tree.cpp -o "$MAIN_BIN"
"$MAIN_BIN" > "$OUTPUT_FILE"

assert_output_contains "Dataset file: $BENCH_DATASET"
assert_output_contains "Query count: 100"
assert_output_contains "========== BENCHMARK WITH N = 100 =========="
assert_output_contains "Configured query count: 100"
assert_output_contains "Configured range size: 7"
assert_output_contains "========== BENCHMARK WITH N = 300 =========="
assert_output_contains "Configured range size: 13"

assert_result_count 100 8
assert_result_count 300 8

assert_output_contains "Scenario=Point B-Tree Linear"
assert_output_contains "Scenario=Point B-Tree Binary"
assert_output_contains "Scenario=Point B+ Tree Linear"
assert_output_contains "Scenario=Point B+ Tree Binary"
assert_output_contains "Scenario=Range B-Tree Linear"
assert_output_contains "Scenario=Range B-Tree Binary"
assert_output_contains "Scenario=Range B+ Tree Linear"
assert_output_contains "Scenario=Range B+ Tree Binary"
assert_output_contains "MeanDiskReads="

echo "PASS: small benchmark completed successfully."
